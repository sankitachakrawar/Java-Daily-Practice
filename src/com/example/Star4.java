package com.example;

public class Star4 {

	public static void main(String[] args) {
		
		int rows=6;
		 for (int i = 1; i <= rows; i++) 
	        {
	            for (int j = i; j >= 1; j--)
	            {
	                System.out.print(j+" ");
	            }
	             
	            System.out.println();
	        }
		
		
	}	
	
}
